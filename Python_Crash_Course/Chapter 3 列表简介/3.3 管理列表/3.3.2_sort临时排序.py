# 创建一个列表[], 然后用sorted(var)进行临时排序
# Owner: Ryan
# Date: 06-Aug-2024

cars = ["bmw", "audi", "toyota", "subaru"]
print("Here's the original list:")
print(cars)

print("\nHere's the sorted list:")
print(sorted(cars))

print("\nHere's the reversed sorted list:")  #
print(sorted(cars))  #


print("\nHere's the original list again:")
print(cars)
