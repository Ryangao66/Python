my_food = ["pizza", "falafel", "carrot cake"]
# [:] list中所有
friend_food = my_food[:]

# 打印列表
print("My favrorite food are:")
print(my_food)
print("\nmy friend's favorite food are:")
print(friend_food)

# 在var中添加内容
my_food.append("cannoli")
friend_food.append("ice cream")

# 在次打印添加之后的列表
print("\nMy favrorite food are after append:")
print(my_food)
print("\nmy friend's favorite food are after append:")
print(friend_food)


